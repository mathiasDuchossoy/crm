<?php

namespace Mondofute\Bundle\GeographieBundle\Entity;
use Mondofute\Bundle\GeographieBundle\Entity\DepartementVideo;
use Mondofute\Bundle\LangueBundle\Entity\Langue;

/**
 * DepartementVideoTraduction
 */
class DepartementVideoTraduction
{
    /**
     * @var int
     */
    private $id;
    /**
     * @var string
     */
    private $libelle;
    /**
     * @var DepartementVideo
     */
    private $video;
    /**
     * @var Langue
     */
    private $langue;

    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Get libelle
     *
     * @return string
     */
    public function getLibelle()
    {
        return $this->libelle;
    }

    /**
     * Set libelle
     *
     * @param string $libelle
     *
     * @return DepartementVideoTraduction
     */
    public function setLibelle($libelle)
    {
        $this->libelle = $libelle;

        return $this;
    }

    /**
     * Get video
     *
     * @return DepartementVideo
     */
    public function getVideo()
    {
        return $this->video;
    }

    /**
     * Set video
     *
     * @param DepartementVideo $video
     *
     * @return DepartementVideoTraduction
     */
    public function setVideo(DepartementVideo $video = null)
    {
        $this->video = $video;

        return $this;
    }

    /**
     * Get langue
     *
     * @return Langue
     */
    public function getLangue()
    {
        return $this->langue;
    }

    /**
     * Set langue
     *
     * @param Langue $langue
     *
     * @return DepartementVideoTraduction
     */
    public function setLangue(Langue $langue = null)
    {
        $this->langue = $langue;

        return $this;
    }
}
